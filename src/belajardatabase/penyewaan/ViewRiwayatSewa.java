    /**
     * Fill in data detail pelanggan from specific pelanggan
     */
    private void showDataDetailOfPelanggan(int selectedRow) {
        String noKTP = "";
        String nama = "";
        String tanggalLahir = "";
        String pekerjaan = "";
        String alamat = "";
        String jenisKelamin = "";
        int idKota = 0;
        String telepon = "";
        String handphone = "";
        String email = "";
        
        int row = selectedRow;
        noKTP = viewTable.getValueAt(row, 2).toString();
        
        pelangganModel.sql.select.reset();
        pelangganModel.sql.select.where("noKTP", "=", noKTP);
        ResultSet rs = pelangganModel.sql.select.execute();
        try {
            if (rs.next() == false) return;
           
            nama = rs.getString("nama");
            tanggalLahir = rs.getString("tanggalLahir");
            pekerjaan = rs.getString("pekerjaan");
            alamat = rs.getString("alamat");
            jenisKelamin = rs.getString("jenisKelamin");
            idKota = rs.getInt("idKota");
            telepon = rs.getString("telepon");
            handphone = rs.getString("handphone");
            email = rs.getString("email");
        } catch (SQLException e) {
            
        }
        mobilModel.sql.getConnection().close(rs);
        
        /* get nama kota from id kota */
        String namaKota = kotaModel.getNamaKotaById(idKota);
        /* get jenis kelamin */
        int jenkel = Integer.parseInt(jenisKelamin);

        field_no_ktp.setText(noKTP);
        field_nama.setText(nama);
        if (jenkel == PelangganTableModel.PEREMPUAN) {
            field_jk.setText("Perempuan");
        }else{
            field_jk.setText("Laki-laki");
        }
        field_tgl_lahir.setText(tanggalLahir);
        field_pekerjaan.setText(pekerjaan);
        field_alamat.setText(alamat);
        field_kota.setText(namaKota);
        field_telepon.setText(telepon);
        field_email.setText(email);
        field_no_hp.setText(handphone);
    }
    
