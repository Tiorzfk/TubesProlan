    public void initModel() {
        ResultSet rs = null;
        
        /* init penyewaan penyewaanModel */
        penyewaanModel = new PenyewaanTableModel();
        
        penyewaanModel.sql.select.where("tanggalKembali", "<>", "0000-00-00");
        penyewaanModel.sql.select.limit(penyewaanModel.dataPerPage);
        penyewaanModel.sql.select.offset(0);
        
        rs = penyewaanModel.sql.select.execute();
        penyewaanModel.save(rs);
        penyewaanModel.sql.getConnection().close(rs);
        
        /* init pelanggan model */
        pelangganModel = new PelangganTableModel();
        /* init mobil model */
        mobilModel = new MobilTableModel();
        /* init kota model */
        kotaModel = new KotaTableModel();
    }
    
    public void initPagination() {
        pagination = new Pagination();
        pagination.setModel(penyewaanModel);
        pagination.setPageLength(penyewaanModel.dataPerPage);
        pagination.GUIComponent.setLabel(positionPaginationLabel);
        pagination.GUIComponent.setComboBox(paginationComboBox);
        pagination.init();
    }
    
     
    public void initTable() {
        viewTable.setModel(penyewaanModel);
        
        // leave only 4 column in table
        for (int i = 4; i < penyewaanModel.getColumnCount(); i++) {
            TableColumn col = viewTable.getColumnModel().getColumn(4);
            viewTable.getColumnModel().removeColumn(col);
        }
        
        updateTable();
    }
          
    private String getPencarianSQLQuery() {
        String cari = pencarianField.getText();
        return "(" +
                    "idSewa like " + quotateWithPercent(cari) + " OR " +
                    "noKTP     like " + quotateWithPercent(cari) +
                ")";
    }
    
    
    public String quotateWithPercent(String content) {
        return "'%" + content + "%'";
    }
    
    public void updateTable() {
        penyewaanModel.fireTableDataChanged();
    }
  
