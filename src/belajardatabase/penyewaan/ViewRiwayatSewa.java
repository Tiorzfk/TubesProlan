    private void pencarianFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pencarianFieldActionPerformed
        // TODO add your handling code here:
        String searchText = pencarianField.getText();

        // reset where clause
        String sign = "USER SEARCH";
        penyewaanModel.sql.select.removeWhereBySegmentSign(sign);

        penyewaanModel.sql.select.whereSegmentStart(sign);
        penyewaanModel.sql.select.where("idSewa", "LIKE", "%" + searchText + "%");
        penyewaanModel.sql.select.orWhere("noKTP", "LIKE", "%" + searchText + "%");
        penyewaanModel.sql.select.whereSegmentEnd(sign);

        int newOffset = pagination.isSQLChanged.updatePagination();
        pagination.isSQLChanged.GUIComponent.reinitComboBox();
        pagination.GUIComponent.update();

        penyewaanModel.sql.select.offset(newOffset);
        ResultSet rs = penyewaanModel.sql.select.execute();
        penyewaanModel.save(rs);
        penyewaanModel.sql.getConnection().close(rs);

        updateTable();
    }//GEN-LAST:event_pencarianFieldActionPerformed

